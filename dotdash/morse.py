def MorsetoText(morse: str) -> str:
    processed_morse = morse.strip().split(" ")
    out = ""

    for i in processed_morse:
        if i in MtoL:
            out += MtoL[i]
        else:
            raise Exception(f"Error translating. No translation for \"{i}\"")

    return out

def TexttoMorse(text: str) -> str:
    out = ""

    for i in text:
        if i.upper() in LtoM:
            out += LtoM[i.upper()]
            out += " "
        else:
            raise Exception(f"Error translating. No Morse available for character \'{i}\'.")

    return out
